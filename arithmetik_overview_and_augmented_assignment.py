# Here we set a variable of friends to the value 0.
friends = 0

# Increas the value of 'friends' by 1 (long version).
# Also known as normal assignment with addition.
friends = friends + 1

# Same operation using a shorthand operator.
# Also known as compound assignment or augmented assignment.
friends += 1

# This works for other operations as well:

# Subtraction (minus).
friends -= 1

# Multiplication (repeating additon).
friends *= 1

# Division (splitting into equal parts).
friends /= 1

# Exponentiation (raising to a power).

friends **= 1

# Since the code runs from top to bottom,
# each operation is applied one after another,
# and the final result is stored in the variable.

print(friends)
