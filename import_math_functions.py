# To use advanced math functions, you have to import the 'math' module.
import math

# You can directly call math functions using math.function_name().
# For example, math.pi is a constant used to calculate circles ()
print(math.pi)

# math.e is another mathematical constant (Euler's number),
# commonly used in physics and for exponential growth calculations.
print(math.e)

# Example: Calculating the square root of an integer.
x = 16
result1 = math.sqrt(x)
print(result1) # Output: 4.0

# Example: Always rounding a float up using math.ceil().
y = 9.1
result2 = math.ceil(y)
print(result2) # Output: 10

# Example: Always rounding a float down using math.floor()-
z = 9.9
result3 = math.floor(z)
print(result3) # Output: 9
